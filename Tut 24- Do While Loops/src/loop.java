class loop {
	public static void main(String[] args) {
		
		int counter = 15;
		
		do {
			System.out.println(counter);
			counter++;
		}while(counter <= 10);
	}
}

/**what the "while loop" does is executes the loop body if the test condition
 * is true, however, the "do while loop" does, is executes the loop body before 
 * checking the test condition
 */
 
